public class Main {
  public static void main(String[] args) {
      // Create an array to store 3 names, loop through array and print out names

      // Declare array of type String
      String[] names = {"Melissa", "Oscar", "Mai"};

      // Loop through array and print out each name in the array
      for (int index = 0; index < names.length; index++) {
          System.out.println(names[index]);
      }
  }
}
